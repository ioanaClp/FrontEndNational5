// arithmetic operators
var a = 10;
var b = 25;

console.log(a + b);
console.log(a - b);
console.log(a / b);
console.log(a * b);
console.log(a % b);

// workshop
console.log(7 % 3);
console.log(10 % 2);
console.log(3 % 4);

// comparison operators
console.log(2 == 2); // true
console.log(2 == '2'); // true
console.log(2 === '2'); // false
console.log(10 == a);

console.log(2 < 3);
console.log(4 <= 3);
console.log(2 > 3);
console.log(2 >= 3);

console.log(typeof a);
console.log(typeof true);
console.log(typeof 12);

// conditional operators
var amCard = true;
var amPin = false;
console.log(amCard && amPin);
console.log(amCard || amPin);

console.log(2 < 2 || 2 == '2'); // false || true = true

